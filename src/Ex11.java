public class Ex11 {
    public static void ghicesteNumarul(int n) {
        switch(n){
            case 1:
                System.out.println("UNU");
                break;
            case 2:
                System.out.println("DOI");
                break;
            case 3:
                System.out.println("TREI");
                break;
            case 4:
                System.out.println("PATRU");
                break;

            case 5:
                System.out.println("CINCI");
                break;
            case 6:
                System.out.println("SASE");
                break;
            case 7:
                System.out.println("SAPTE");
                break;
            case 8:
                System.out.println("OPT");
                break;
            case 9:
                System.out.println("NOUA");
                break;
            default:
                System.out.println("Numar gresit, nu este cifra");
                break;
    }
}}
